/**
 * Freezes the WHOLE serialized {@see \Milpa\Resolver\Report\ResolutionReport} as a public contract —
 * not just its lists — so the report can never decay into "a bag of opaque arrays". Four layers:
 *
 *  1. LISTS — each of the nine lists has one closed schema: an exact, ordered key set, a type per
 *     field, and a closed value domain where one applies (`kind`, `level`, `via`, catalog-bound
 *     `code`).
 *  2. SHELL — the top-level key ORDER is frozen ({@see topLevelKeys()}), `status` is a closed scalar
 *     domain (the four {@see ResolutionStatus} values, all exercised by the corpus), and `metadata`
 *     is a frozen two-field map ({@see metadataSchema()}).
 *  3. NESTED errors[] shapes — `context` keys follow one endorsed order when present
 *     ({@see contextKeyOrder()}; `MILPA_MANIFEST_DRIFT` is exempt, see there), every
 *     `recommendedActions` entry is `type`-first with a closed type domain ({@see actionTypes()}),
 *     and `learn` is the bilingual links map ({@see learnKeys()}).
 *  4. ROUND-TRIP — `toArray(fromArray(toArray($r)))` is byte-identical to `toArray($r)` for the WHOLE
 *     corpus, and where `fromArray()` is deliberately lossy (defensive drops of malformed input,
 *     `recommendedActions` re-derivation) the loss is frozen EXPLICITLY by its own tests.
 *
 * Every example is GENERATED by the engine over the five §15 scenarios plus the legacy/CRM cases —
 * never a hand-written fixture — so every schema is asserted against what the engine actually emits.
 * A test fails on a missing key, an extra undocumented key (both mutation directions are exercised),
 * a wrong type, an out-of-domain value, or a reordered key set; further tests assert the README
 * "Report shapes" tables list exactly these fields and domains, so docs cannot drift from code.
 */
final class ReportShapeContractsTest extends TestCase
{
