        self::assertContains($drift['code'], self::CONTEXT_ORDER_EXEMPT);
        self::assertSame(['package', 'fields'], array_keys($drift['context']));
        self::assertNotSame([], $this->contextViolations($drift['context']), 'the exemption would be vacuous if the drift context already obeyed the engine order');
    }

    /**
