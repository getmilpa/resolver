        foreach (array_keys(self::schema()) as $list) {
            $entry = $this->entriesFor($list)[0];
            $firstKey = array_key_first($entry);
            $mutated = $entry;
            unset($mutated[$firstKey]);

            self::assertNotSame([], $this->schemaViolations($list, $mutated), "{$list}[] accepted a missing key");
        }
    }

    /**
     * Mutation check, type direction — a value of the wrong type makes the entry fail its schema.
     */
    public function testSchemaRejectsAWronglyTypedValue(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            $entry = $this->entriesFor($list)[0];
            $firstKey = array_key_first($entry);
            $mutated = $entry;
            // A nested array is never a valid value for any first field of any list (all are scalars).
            $mutated[$firstKey] = ['unexpected' => 'array'];

            self::assertNotSame([], $this->schemaViolations($list, $mutated), "{$list}[] accepted a wrong type");
        }
    }

    /**
     * Drift-lock on `warnings[].fallback` (Contrato slice T3), both mutation directions on the field
     * itself: a suggested-capability warning that LOSES the key fails the schema, and a non-string
     * value fails its `?string` type. The corpus assertion below proves the field is exercised with a
     * real (non-null) engine-emitted value, so the lock is never asserted over thin air.
     */
    public function testWarningsFallbackIsDriftLocked(): void
    {
        $entry = null;
        foreach ($this->entriesFor('warnings') as $candidate) {
            if ($candidate['kind'] === 'suggested-capability') {
                $entry = $candidate;

                break;
            }
        }
        self::assertNotNull($entry, 'corpus produced no suggested-capability warning');

        $missingKey = $entry;
        unset($missingKey['fallback']);
        self::assertNotSame([], $this->schemaViolations('warnings', $missingKey), 'warnings[] accepted a missing fallback key');

        $wrongType = $entry;
        $wrongType['fallback'] = 123;
        self::assertNotSame([], $this->schemaViolations('warnings', $wrongType), 'warnings[] accepted a non-string fallback');
    }

    /**
     * The corpus exercises a NON-NULL fallback — a suggestion record that declares its degradation
     * path — so `?string` is proven against a real value, not just the null default.
     */
    public function testTheCorpusExercisesADeclaredFallback(): void
    {
        $fallbacks = array_column($this->entriesFor('warnings'), 'fallback');

        self::assertContains('noop', $fallbacks, 'corpus never produced a warnings[].fallback != null');
        self::assertContains(null, $fallbacks, 'corpus never produced a warnings[].fallback = null');
    }

    /**
     * Mutation check, domain direction — an out-of-domain value for a closed field fails the schema.
     */
    public function testSchemaRejectsAnOutOfDomainValue(): void
    {
        $entry = $this->entriesFor('missing')[0];
        $mutated = $entry;
        $mutated['kind'] = 'not-a-real-kind';

        self::assertNotSame([], $this->schemaViolations('missing', $mutated), 'missing[] accepted an out-of-domain kind');
    }

    /**
     * The README "Report shapes" tables list EXACTLY the frozen fields of each list, in order, AND
     * each field's Type cell normalizes to the exact `[type, domain?]` spec the schema enforces
     * ({@see readmeTypeSpec()}) — the public contract cannot drift from this test's schema in name
     * OR in type.
     */
    public function testReadmeTablesMatchTheFrozenSchemaExactly(): void
    {
        $documented = $this->readmeFieldsByList();

        foreach (self::schema() as $list => $fields) {
            self::assertArrayHasKey($list, $documented, "README has no Report-shapes table for {$list}[]");
            self::assertSame(
                array_keys($fields),
                array_keys($documented[$list]),
                "README {$list}[] table fields drifted from the schema",
            );
            foreach ($fields as $field => $spec) {
                self::assertSame(
                    $spec,
                    self::readmeTypeSpec($documented[$list][$field]),
                    "README {$list}[].{$field} type drifted from the schema",
                );
            }
        }

        self::assertSame(
            array_keys(self::schema()),
            array_keys($documented),
            'README documents a different set of lists than the schema freezes',
        );
    }

