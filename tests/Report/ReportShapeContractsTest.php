    /**
     * The `kind`/`via` values every list is required to exercise, so the schema is proven against the
     * whole domain — not just the one branch that happens to run first.
     *
     * @return array<string, array{field: string, values: list<string>}>
     */
    private static function requiredDomainCoverage(): array
    {
        return [
            'missing' => ['field' => 'kind', 'values' => ['contract', 'capability', 'surface-requirement', 'legacy-contract']],
            'conflicts' => ['field' => 'kind', 'values' => ['capability', 'dependency-cycle']],
            'warnings' => ['field' => 'kind', 'values' => ['suggested-capability', 'deprecation', 'surface', 'risk-expiry']],
