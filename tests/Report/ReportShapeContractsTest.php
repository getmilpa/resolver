/**
 * Freezes the shape of every entry in every {@see \Milpa\Resolver\Report\ResolutionReport} list, so
 * the report can never decay into "a bag of opaque arrays". Each list has one closed schema: an exact,
 * ordered key set, a type per field, and a closed value domain where one applies (`kind`, `level`,
 * `via`, and catalog-bound `code`). Every example is GENERATED by the engine over the five §15
 * scenarios plus the legacy/CRM cases — never a hand-written fixture — so the schema is asserted
 * against what the engine actually emits. A test fails on a missing key, an extra undocumented key
 * (both mutation directions are exercised), a wrong type, or an out-of-domain value; a further test
 * asserts the README "Report shapes" tables list exactly these fields, so docs cannot drift from code.
 */
final class ReportShapeContractsTest extends TestCase
{
    /**
     * The frozen schema per report list: `list => [field => [type, domain?]]`, in emission order.
     *
     * Type tokens: `string`, `non-empty-string`, `?string` (string or null), `bool`, `string[]`
     * (list of strings), `array[]` (list of arrays), `array` (free-form map). An optional second
     * element closes the value domain: an explicit list of allowed strings, or the sentinel
     * `catalog` (the value must be a known {@see ErrorCatalog} code).
