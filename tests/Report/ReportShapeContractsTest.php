    public function testEveryEntryHasExactlyTheFrozenKeySetInOrder(): void
    {
        foreach (self::schema() as $list => $fields) {
            $expected = array_keys($fields);
            foreach ($this->entriesFor($list) as $i => $entry) {
                self::assertSame($expected, array_keys($entry), "{$list}[{$i}] key set drifted");
            }
        }
    }

    /**
     * Every field of every entry matches its frozen type and (where closed) its value domain.
