    /**
     * Mutation check — REORDERING the top-level keys of a serialized clone fails the shell, even with
     * every key and value intact. The order IS the contract: the same report must render byte-for-byte
     * identically everywhere, with the diagnosis (`errors`) always right after `status`.
     */
    public function testShellRejectsReorderedTopLevelKeys(): void
    {
        $report = $this->corpus()[0];
        $reordered = ['metadata' => $report['metadata']] + $report;

        $canonicalContent = $report;
        ksort($canonicalContent);
        $reorderedContent = $reordered;
        ksort($reorderedContent);
        self::assertSame($canonicalContent, $reorderedContent, 'the mutation must only reorder, never change content');

        self::assertNotSame([], $this->shellViolations($reordered), 'shell accepted reordered top-level keys');
    }

