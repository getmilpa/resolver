        foreach (array_keys(self::schema()) as $list) {
            self::assertNotSame([], $this->entriesFor($list), "corpus produced no {$list}[] entries");
        }
    }

    /**
     * The corpus exercises every `kind`/`via` value the closed domains name, so the schema is frozen
