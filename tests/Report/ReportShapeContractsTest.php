     * by the engine's errorContext() pass, and carries its own frozen shape — exactly `package` (the
     * drifted package label) and `fields` (the drifted field records) — outside the engine order on
     * purpose: a drift diagnosis identifies a manifest, not a graph requirement.
     */
    private const CONTEXT_ORDER_EXEMPT = ['MILPA_MANIFEST_DRIFT'];
