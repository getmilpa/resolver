    /**
     * Validate a serialized report's SHELL: exact top-level key order, closed status domain, frozen
     * metadata map (exact ordered keys + types). Key-order drift short-circuits, exactly like the
     * per-list validator.
     *
     * @param array<string, mixed> $report
     *
     * @return list<string>
     */
    private function shellViolations(array $report): array
    {
        if (array_keys($report) !== self::topLevelKeys()) {
            return [sprintf('top-level keys [%s] != frozen order [%s]', implode(',', array_keys($report)), implode(',', self::topLevelKeys()))];
        }

        $violations = [];
        if (!is_string($report['status']) || !in_array($report['status'], self::statusDomain(), true)) {
            $violations[] = sprintf('status: value "%s" is out of domain', is_scalar($report['status']) ? (string) $report['status'] : get_debug_type($report['status']));
        }

        if (!is_array($report['metadata'])) {
            $violations[] = sprintf('metadata: expected map, got %s', get_debug_type($report['metadata']));

            return $violations;
        }
        if (array_keys($report['metadata']) !== array_keys(self::metadataSchema())) {
            $violations[] = sprintf('metadata keys [%s] != frozen [%s]', implode(',', array_keys($report['metadata'])), implode(',', array_keys(self::metadataSchema())));

            return $violations;
        }
        foreach (self::metadataSchema() as $field => $type) {
            if (!$this->typeOk($type, $report['metadata'][$field])) {
                $violations[] = sprintf('metadata.%s: expected type %s, got %s', $field, $type, get_debug_type($report['metadata'][$field]));
            }
        }

        return $violations;
    }

    /**
     * Validate an engine-emitted context against the endorsed key order: every key known, positions
     * strictly ascending (a subsequence of the order), types right, and the two anchors — `id` and
     * `hostProfile` — always present.
     *
     * @param array<string, mixed> $context
     *
     * @return list<string>
     */
    private function contextViolations(array $context): array
    {
        $order = self::contextKeyOrder();
        $types = self::contextFieldTypes();

        $violations = [];
        foreach (['id', 'hostProfile'] as $anchor) {
            if (!array_key_exists($anchor, $context)) {
                $violations[] = sprintf('context is missing its `%s` anchor', $anchor);
            }
        }

        $lastPosition = -1;
        foreach ($context as $key => $value) {
            $position = array_search($key, $order, true);
            if ($position === false) {
                $violations[] = sprintf('context key `%s` is not in the endorsed order', $key);

                continue;
            }
            if ($position <= $lastPosition) {
                $violations[] = sprintf('context key `%s` is out of the endorsed order', $key);
            }
            $lastPosition = $position;

            if (!$this->typeOk($types[$key], $value)) {
                $violations[] = sprintf('context.%s: expected type %s, got %s', $key, $types[$key], get_debug_type($value));
            }
        }

        return $violations;
    }

    /**
     * Validate one recommendedActions entry: `type` FIRST, inside the closed type domain, and every
     * parameter a string or a list of strings.
     *
     * @param array<string, mixed> $action
     *
     * @return list<string>
     */
    private function actionViolations(array $action): array
    {
        if (array_key_first($action) !== 'type') {
            return [sprintf('action leads with `%s`, not `type`', (string) array_key_first($action))];
        }

        $violations = [];
        if (!is_string($action['type']) || !in_array($action['type'], self::actionTypes(), true)) {
            $violations[] = sprintf('action type "%s" is out of domain', is_scalar($action['type']) ? (string) $action['type'] : get_debug_type($action['type']));
        }

        foreach ($action as $key => $value) {
            if ($key === 'type') {
                continue;
            }
            if (!$this->typeOk('string-or-string[]', $value)) {
                $violations[] = sprintf('action.%s: expected a string or list of strings, got %s', $key, get_debug_type($value));
            }
        }

        return $violations;
    }

    /**
     * Validate one learn map: keys a subsequence of the frozen order, `academy` + `llms` required,
     * every value the exact bilingual `{es, en}` pair of non-empty strings.
     *
     * @param array<string, mixed> $learn
     *
     * @return list<string>
     */
    private function learnViolations(array $learn): array
    {
        $order = self::learnKeys();

        $violations = [];
        foreach (['academy', 'llms'] as $required) {
            if (!array_key_exists($required, $learn)) {
                $violations[] = sprintf('learn is missing its required `%s` link', $required);
            }
        }

        $lastPosition = -1;
        foreach ($learn as $key => $value) {
            $position = array_search($key, $order, true);
            if ($position === false) {
                $violations[] = sprintf('learn key `%s` is not in the frozen set', $key);

                continue;
            }
            if ($position <= $lastPosition) {
                $violations[] = sprintf('learn key `%s` is out of the frozen order', $key);
            }
            $lastPosition = $position;

            if (!is_array($value) || array_keys($value) !== ['es', 'en']) {
                $violations[] = sprintf('learn.%s: expected the exact bilingual {es, en} pair', $key);

                continue;
            }
            foreach ($value as $lang => $url) {
                if (!is_string($url) || $url === '') {
                    $violations[] = sprintf('learn.%s.%s: expected a non-empty URL', $key, $lang);
                }
            }
        }

        return $violations;
    }

