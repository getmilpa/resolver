    /**
     * Validate one entry against its list schema; returns a list of human-readable violations (empty
     * when the entry conforms). Key-set drift short-circuits: once the shape is wrong, per-field type
     * noise would only obscure the real fault.
     *
     * @param array<string, mixed> $entry
     *
     * @return list<string>
     */
    private function schemaViolations(string $list, array $entry): array
    {
        $schema = self::schema()[$list];
        $expectedKeys = array_keys($schema);
        if (array_keys($entry) !== $expectedKeys) {
            return [sprintf('keys [%s] != expected [%s]', implode(',', array_keys($entry)), implode(',', $expectedKeys))];
        }

        $violations = [];
        foreach ($schema as $field => $spec) {
            $type = $spec[0];
            $domain = $spec[1] ?? null;
            $value = $entry[$field];

            if (!$this->typeOk($type, $value)) {
                $violations[] = sprintf('%s: expected type %s, got %s', $field, $type, get_debug_type($value));

                continue;
            }

            if ($domain !== null && is_string($value)) {
                $allowed = $domain === 'catalog' ? ErrorCatalog::codes() : $domain;
                if (!in_array($value, $allowed, true)) {
                    $violations[] = sprintf('%s: value "%s" is out of domain', $field, $value);
                }
            }
        }

        return $violations;
    }

    private function typeOk(string $type, mixed $value): bool
    {
        return match ($type) {
            'string' => is_string($value),
            'non-empty-string' => is_string($value) && $value !== '',
            '?string' => $value === null || is_string($value),
            'bool' => is_bool($value),
            'string[]' => $this->isListOf($value, 'is_string'),
            'array[]' => $this->isListOf($value, 'is_array'),
            'array' => is_array($value),
            default => false,
        };
    }

    /**
     * @param callable(mixed): bool $predicate
     */
    private function isListOf(mixed $value, callable $predicate): bool
    {
        if (!is_array($value) || !array_is_list($value)) {
            return false;
        }
        foreach ($value as $item) {
            if (!$predicate($item)) {
                return false;
            }
        }

        return true;
    }

