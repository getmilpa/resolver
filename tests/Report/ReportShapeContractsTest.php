    /**
     * Validate one entry against its list schema; returns a list of human-readable violations (empty
     * when the entry conforms). Key-set drift short-circuits: once the shape is wrong, per-field type
     * noise would only obscure the real fault.
     *
     * @param array<string, mixed> $entry
     *
     * @return list<string>
     */
    private function schemaViolations(string $list, array $entry): array
    {
        $schema = self::schema()[$list];
        $expectedKeys = array_keys($schema);
        if (array_keys($entry) !== $expectedKeys) {
            return [sprintf('keys [%s] != expected [%s]', implode(',', array_keys($entry)), implode(',', $expectedKeys))];
        }

        $violations = [];
        foreach ($schema as $field => $spec) {
            $type = $spec[0];
            $domain = $spec[1] ?? null;
            $value = $entry[$field];

            if (!$this->typeOk($type, $value)) {
                $violations[] = sprintf('%s: expected type %s, got %s', $field, $type, get_debug_type($value));

                continue;
            }

            if ($domain !== null && is_string($value)) {
                $allowed = $domain === 'catalog' ? ErrorCatalog::codes() : $domain;
                if (!in_array($value, $allowed, true)) {
                    $violations[] = sprintf('%s: value "%s" is out of domain', $field, $value);
                }
            }
        }

        return $violations;
    }

    private function typeOk(string $type, mixed $value): bool
    {
        return match ($type) {
            'string' => is_string($value),
            'non-empty-string' => is_string($value) && $value !== '',
            '?string' => $value === null || is_string($value),
            'bool' => is_bool($value),
            'string[]' => $this->isListOf($value, 'is_string'),
            'string-or-string[]' => is_string($value) || $this->isListOf($value, 'is_string'),
            'array[]' => $this->isListOf($value, 'is_array'),
            'array' => is_array($value),
            default => false,
        };
    }

    /**
     * @param callable(mixed): bool $predicate
     */
    private function isListOf(mixed $value, callable $predicate): bool
    {
        if (!is_array($value) || !array_is_list($value)) {
            return false;
        }
        foreach ($value as $item) {
            if (!$predicate($item)) {
                return false;
            }
        }

        return true;
    }

    /**
     * Parse the README "Report shapes" section: for each `#### `list[]`` subsection, the ordered
     * `field => Type cell` map taken from the first two columns of its table (the raw Type cell —
     * escaped pipes included — is normalized by {@see readmeTypeSpec()}).
     *
     * @return array<string, array<string, string>>
     */
    private function readmeFieldsByList(): array
    {
        $md = file_get_contents(dirname(__DIR__, 2) . '/README.md');
        self::assertIsString($md, 'README.md is unreadable');

        $start = strpos($md, '## Report shapes');
        self::assertNotFalse($start, 'README has no "## Report shapes" section');
        $section = substr($md, $start);
        $nextH2 = strpos($section, "\n## ", 4);
        if ($nextH2 !== false) {
            $section = substr($section, 0, $nextH2);
        }

        preg_match_all('/####\s+`([a-zA-Z]+)\[\]`(.*?)(?=\n####|\z)/s', $section, $blocks, PREG_SET_ORDER);

        $out = [];
        foreach ($blocks as $block) {
            $list = $block[1];
            preg_match_all('/^\|\s*`([^`]+)`\s*\|\s*((?:[^|\\\\]|\\\\.)*?)\s*\|/m', $block[2], $rows, PREG_SET_ORDER);
            $out[$list] = [];
            foreach ($rows as $row) {
                $out[$list][$row[1]] = $row[2];
            }
        }

        return $out;
    }

    /**
     * Translate one README Type cell into the schema's `[type, domain?]` spec, so the parity test
     * compares TYPES, not just field names. Three notations, nothing else: a lone backticked token
     * is the type itself (`non-empty-string`, `?string`, `bool`, `string[]`, `array[]`, `array`,
     * `string`); `<type> (catalog)` marks the catalog-bound value domain; and two or more literal
     * values separated by `\|` are the closed-domain notation — a `non-empty-string` restricted to
     * exactly those values, in that order.
     *
     * @return array{0: string, 1?: list<string>|string}
     */
    private static function readmeTypeSpec(string $cell): array
    {
        $tokens = array_map(
            static fn (string $token): string => trim($token, '` '),
            preg_split('/\s*\\\\\|\s*/', trim($cell)) ?: [],
        );

        if (count($tokens) > 1) {
            return ['non-empty-string', $tokens];
        }

        if (str_ends_with($tokens[0], ' (catalog)')) {
            return [substr($tokens[0], 0, -strlen(' (catalog)')), 'catalog'];
        }

        return [$tokens[0]];
    }

