    /**
     * Every list carries at least one engine-generated entry, so no schema is asserted over thin air.
     */
    public function testTheCorpusExercisesEveryList(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            self::assertNotSame([], $this->entriesFor($list), "corpus produced no {$list}[] entries");
        }
    }

    /**
     * The corpus exercises every `kind`/`via` value the closed domains name, so the schema is frozen
     * against the full branch space of each list.
     */
    public function testTheCorpusCoversEveryClosedDomainValue(): void
    {
        foreach (self::requiredDomainCoverage() as $list => $coverage) {
            $seen = array_column($this->entriesFor($list), $coverage['field']);
            foreach ($coverage['values'] as $value) {
                self::assertContains($value, $seen, "corpus never produced {$list}[].{$coverage['field']} = {$value}");
            }
        }
    }

    /**
     * Headline contract: every entry of every list has EXACTLY the frozen key set, in order
     * (`assertSame` on `array_keys`). A missing or extra key is caught here.
     */
    public function testEveryEntryHasExactlyTheFrozenKeySetInOrder(): void
    {
        foreach (self::schema() as $list => $fields) {
            $expected = array_keys($fields);
            foreach ($this->entriesFor($list) as $i => $entry) {
                self::assertSame($expected, array_keys($entry), "{$list}[{$i}] key set drifted");
            }
        }
    }

    /**
     * Every field of every entry matches its frozen type and (where closed) its value domain.
     */
    public function testEveryEntryMatchesItsTypesAndDomains(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            foreach ($this->entriesFor($list) as $i => $entry) {
                self::assertSame([], $this->schemaViolations($list, $entry), "{$list}[{$i}] violated its schema");
            }
        }
    }

    /**
     * Mutation check, direction 1 — an EXTRA undocumented key makes the entry fail its schema. Proves
     * the contract rejects silent field growth (the exact decay Rod's review flagged).
     */
    public function testSchemaRejectsAnExtraUndocumentedKey(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            $entry = $this->entriesFor($list)[0];
            $mutated = $entry + ['undocumentedField' => 'sneaky'];

            self::assertNotSame([], $this->schemaViolations($list, $mutated), "{$list}[] accepted an extra key");
        }
    }

    /**
     * Mutation check, direction 2 — a MISSING required key makes the entry fail its schema.
     */
    public function testSchemaRejectsAMissingRequiredKey(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            $entry = $this->entriesFor($list)[0];
            $firstKey = array_key_first($entry);
            $mutated = $entry;
            unset($mutated[$firstKey]);

            self::assertNotSame([], $this->schemaViolations($list, $mutated), "{$list}[] accepted a missing key");
        }
    }

    /**
     * Mutation check, type direction — a value of the wrong type makes the entry fail its schema.
     */
    public function testSchemaRejectsAWronglyTypedValue(): void
    {
        foreach (array_keys(self::schema()) as $list) {
            $entry = $this->entriesFor($list)[0];
            $firstKey = array_key_first($entry);
            $mutated = $entry;
            // A nested array is never a valid value for any first field of any list (all are scalars).
            $mutated[$firstKey] = ['unexpected' => 'array'];

            self::assertNotSame([], $this->schemaViolations($list, $mutated), "{$list}[] accepted a wrong type");
        }
    }

    /**
     * Mutation check, domain direction — an out-of-domain value for a closed field fails the schema.
     */
    public function testSchemaRejectsAnOutOfDomainValue(): void
    {
        $entry = $this->entriesFor('missing')[0];
        $mutated = $entry;
        $mutated['kind'] = 'not-a-real-kind';

        self::assertNotSame([], $this->schemaViolations('missing', $mutated), 'missing[] accepted an out-of-domain kind');
    }

    /**
     * The README "Report shapes" tables list EXACTLY the frozen fields of each list, in order — the
     * public contract cannot drift from the schema this test enforces.
     */
    public function testReadmeTablesMatchTheFrozenSchemaExactly(): void
    {
        $documented = $this->readmeFieldsByList();

        foreach (self::schema() as $list => $fields) {
            self::assertArrayHasKey($list, $documented, "README has no Report-shapes table for {$list}[]");
            self::assertSame(
                array_keys($fields),
                $documented[$list],
                "README {$list}[] table fields drifted from the schema",
            );
        }

        self::assertSame(
            array_keys(self::schema()),
            array_keys($documented),
            'README documents a different set of lists than the schema freezes',
        );
    }

