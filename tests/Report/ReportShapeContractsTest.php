    /**
     * Collect every engine-generated entry for one report list across the whole corpus.
     *
     * @return list<array<string, mixed>>
     */
    private function entriesFor(string $list): array
    {
        $out = [];
        foreach ($this->corpus() as $report) {
            foreach ($report[$list] as $entry) {
                self::assertIsArray($entry);
                /** @var array<string, mixed> $entry */
                $out[] = $entry;
            }
        }

        return $out;
    }

    /**
     * The engine-generated corpus: each case is resolved and serialized, together covering every list,
     * every `kind`, and every `via`.
     *
     * @return list<array<string, mixed>>
     */
    private function corpus(): array
    {
        $resolver = new GraphResolver();
        $reports = [];
        foreach ($this->inputs() as $input) {
            $reports[] = $resolver->resolve($input)->toArray();
        }

        return $reports;
    }

