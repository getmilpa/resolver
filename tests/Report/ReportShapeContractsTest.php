        $md = file_get_contents(dirname(__DIR__, 2) . '/README.md');
        self::assertIsString($md, 'README.md is unreadable');

        $start = strpos($md, '## Report shapes');
        self::assertNotFalse($start, 'README has no "## Report shapes" section');
        $section = substr($md, $start);
        $nextH2 = strpos($section, "\n## ", 4);
        if ($nextH2 !== false) {
            $section = substr($section, 0, $nextH2);
        }

        preg_match_all('/####\s+`([a-zA-Z]+)\[\]`(.*?)(?=\n####|\z)/s', $section, $blocks, PREG_SET_ORDER);

        $out = [];
        foreach ($blocks as $block) {
            $list = $block[1];
            preg_match_all('/^\|\s*`([^`]+)`\s*\|/m', $block[2], $rows);
            $out[$list] = $rows[1];
        }

        return $out;
    }
