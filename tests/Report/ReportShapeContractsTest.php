    // --- the nested errors[] shapes: context, recommendedActions, learn -----------------------------

    /**
     * Every engine-emitted `errors[].context` follows the endorsed key order: present keys are a
     * subsequence of {@see contextKeyOrder()}, correctly typed, with the requirement identity (`id`)
     * and the host label always present. Caller-emitted MILPA_MANIFEST_DRIFT is exempt (see
     * {@see CONTEXT_ORDER_EXEMPT}) — and never emitted by the engine, so the whole corpus is checked.
     */
    public function testEngineEmittedContextsFollowTheEndorsedKeyOrder(): void
    {
        foreach ($this->errorEntries() as $i => $error) {
            self::assertIsString($error['code']);
            self::assertNotContains($error['code'], self::CONTEXT_ORDER_EXEMPT, 'the engine corpus must never emit a caller-emitted code');
            self::assertIsArray($error['context']);

            self::assertSame([], $this->contextViolations($error['context']), "errors[{$i}] context drifted from the endorsed order");
        }
    }

    /**
     * The corpus exercises EVERY endorsed context key somewhere, so the order is proven over the full
     * key space — `oneOf` and `providedBy` ride together on the oneOf version miss, `fallback` on the
     * declared degradation, `surface` on the surface miss — never over thin air.
     */
    public function testTheCorpusExercisesEveryEndorsedContextKey(): void
    {
        $seen = [];
        foreach ($this->errorEntries() as $error) {
            self::assertIsArray($error['context']);
            foreach (array_keys($error['context']) as $key) {
                $seen[$key] = true;
            }
        }

        foreach (self::contextKeyOrder() as $key) {
            self::assertArrayHasKey($key, $seen, "corpus never produced a context carrying `{$key}`");
        }
    }

    /**
     * Mutation checks — a context that DISORDERS two endorsed keys fails, and so does one carrying a
     * key outside the endorsed set.
     */
    public function testContextRejectsDisorderedOrUnknownKeys(): void
    {
        $context = null;
        foreach ($this->errorEntries() as $error) {
            if (is_array($error['context']) && count($error['context']) >= 3) {
                $context = $error['context'];

                break;
            }
        }
        self::assertNotNull($context, 'corpus produced no context with three keys to mutate');

        $keys = array_keys($context);
        $swapped = $context;
        [$swapped[$keys[0]], $swapped[$keys[1]]] = [$context[$keys[1]], $context[$keys[0]]];
        $disordered = [];
        foreach ([$keys[1], $keys[0], ...array_slice($keys, 2)] as $key) {
            $disordered[$key] = $context[$key];
        }
        self::assertNotSame([], $this->contextViolations($disordered), 'context accepted disordered endorsed keys');

        $unknown = $context + ['undocumentedField' => 'sneaky'];
        self::assertNotSame([], $this->contextViolations($unknown), 'context accepted an unknown key');
    }

    /**
     * The documented exemption is real and itself frozen: a caller-emitted MILPA_MANIFEST_DRIFT error
     * (built by DriftDetector, never the engine) carries EXACTLY `package` + `fields` — keys outside
     * the endorsed engine order on purpose, and pinned here so the exemption cannot silently widen.
     */
    public function testManifestDriftContextIsTheExemptFrozenShape(): void
    {
        $drift = ErrorCatalog::for('MILPA_MANIFEST_DRIFT', [
            'package' => 'OAuthPlugin',
            'fields' => [['field' => 'version', 'declared' => '1.0.0', 'actual' => '2.0.0']],
